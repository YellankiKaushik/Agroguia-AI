export function formatIndianNumber(num: number | string, _lang: Language): string {
  const n = typeof num === 'string' ? parseFloat(num.replace(/,/g, '')) : num
  if (isNaN(n)) return String(num)
  return n.toLocaleString('en-IN')
}

